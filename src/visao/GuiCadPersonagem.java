private void cadastrar() {
        try {
            PersonagemVO pCA = new PersonagemVO();
            pCA.setNome(jtNome.getText());
            pCA.setSexo(bgSexo.getSelection().getActionCommand());
            pCA.setIdade(Integer.parseInt(jtIdade.getText()));
            pCA.setPokemonsCapturado(Integer.parseInt(jtCapturado.getText()));
            pCA.setCidade(bgCidade.getSelection().getActionCommand());
            pCA.setInsignia(bgInsig.getSelection().getActionCommand());
            pCA.setSaffari(bgSimNao.getSelection().getActionCommand());
            
          
            //Enviando objeto pVO para ser persistido no banco de dados
            PersonagemServicos ps = servicos.ServicosFactory.getPersonagemServicos();
            ps.cadastrarPersonagem(pCA);

            //Mensagem de sucesso!
            JOptionPane.showMessageDialog(this,
                    "Personagem cadastrado com sucesso!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "ERRO! " + e.getMessage());
        }//fecha catch
    }//fecha método

    private void limpar() {
        jtNome.setText("");
        jtIdade.setText("");
        jtCapturado.setText("");
       

        //Limpando os RadioButton
        bgCidade.clearSelection();
        bgSexo.clearSelection();
        bgSimNao.clearSelection();
        bgCidade.clearSelection();
    }//fecha método     
