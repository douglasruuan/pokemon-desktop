    private void jtNomeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtNomeFocusLost
        if (jtNome.getText().isEmpty()
                || jtNome.getText().length() < 1
                || jtNome.getText().length() > 30) {

            jlNomeErro.setIcon(new ImageIcon(getClass().getResource("/imagens/erro.png")));

        } else {

            jlNomeErro.setIcon(new ImageIcon(getClass().getResource("/imagens/certo.png")));
        }
    }//GEN-LAST:event_jtNomeFocusLost

